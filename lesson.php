<script>


    $('.lesson-sidebar-btn').on("click" , function() {
        $('.lesson-sidebar-area').toggleClass('active');
        if ($('.lesson-sidebar-area').hasClass('active')) {
            $('.lesson-sidebar-btn i').addClass('fa-chevron-right').removeClass('fa-chevron-left');
        } else {
            $('.lesson-sidebar-btn i').removeClass('fa-chevron-right').addClass('fa-chevron-left');
        }
    });
    $('.lesson-sidebar-cls-btn').on('click' , function() {
        $('.lesson-sidebar-area').removeClass('active');
    });

    // faqs js

    $(".faq-text-area").hide();
    $(".faq-box.active").find(".faq-text-area").show();

    $(".faq-btn").click(function() {
        var $currentFaqBox = $(this).closest(".faq-box");

        if (!$currentFaqBox.hasClass("active")) {
            $(".faq-text-area").slideUp();
            $(".faq-box").removeClass("active");
            $currentFaqBox.addClass("active");
            $currentFaqBox.find(".faq-text-area").slideDown();
        } else {
            $currentFaqBox.removeClass("active");
            $currentFaqBox.find(".faq-text-area").slideUp();
        }
    });
</script>